# parameter assignment
# $1 start remark line
# $2 language
# $3 assignment operator or empty


function tsc_write_spec(){
	$TSC_SBP_ECHOE "write" $4 "for ("$2")"
	$TSC_SBP_ECHOE $1"\n"$1" Spec-File("$2") for "$2"\n"$1 > $4
	$TSC_SBP_ECHOE "Summary: "$TSC_PRJG_PSUMS "("$2")" >> $4
	$TSC_SBP_ECHOE "Version: "$TSC_SPEC_VERSION >> $4
	$TSC_SBP_ECHOE "Release: "$TSC_SPEC_RELEASE >> $4
	$TSC_SBP_ECHOE "License: "$TSC_PRJG_PLIC >> $4
	$TSC_SBP_ECHOE "Group: "$TSC_PRJG_PGRP >> $4
	$TSC_SBP_ECHOE "Source: https://github.com/Chartwalker/${TSC_SPEC_TGZ}" >> $4
	$TSC_SBP_ECHOE "URL: https://github.com/Chartwalker/${TSC_SPEC_URL}" >> $4
	$TSC_SBP_ECHOE "\n%description: see manual pdf at https://github.com/Chartwalker/${TSC_SPEC_DOC}" >> $4

}

# parameter assignment
# $1 start remark line
# $2 language
# $3 assignment operator or empty


function tsc_write_snapyaml(){
	$TSC_SBP_ECHOE "write" $4 "for ("$2")"
	$TSC_SBP_ECHOE "name: "$TSC_PRJG_FNAME > $4
	$TSC_SBP_ECHOE "base: core24" >> $4
	$TSC_SBP_ECHOE "version: "${TSC_SNAPYAML_VERSION} >> $4
	$TSC_SBP_ECHOE "summary: "$TSC_PRJG_PSUMS >> $4
	$TSC_SBP_ECHOE "description: |" >> $4
	$TSC_SBP_ECHOE "  "$TSC_PRJG_PSUML >> $4
	$TSC_SBP_ECHOE "grade: "${TSC_SNAPYAML_GRADE} >> $4
	$TSC_SBP_ECHOE "confinement: "${TSC_SNAPYAML_CONFINEMENT} >> $4
}

