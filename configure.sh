# parameter assignment for writing files

# $1 start definition line #define or empty
# $2 start remark line
# $3 start action line
# $4 assignment operator or empty
# $5 outputfile

for ((y=0; y<3; y++))
do
	tsc_write_env "" "#" "" "=" $TSC_CDPA_CONF$TSC_SLASH${TSC_PRJG_VERSION[$y,5]}$TSC_SLASH$TSC_CFN_CMK
	tsc_write_env "#define " "//" " " " " $TSC_CDPA_CONF$TSC_SLASH${TSC_PRJG_VERSION[$y,5]}$TSC_SLASH$TSC_CFN_CH
done

for ((i=0; i<3; i++))
do 
	TSC_CPFN_CMK="$TSC_CDPA_CONF$TSC_SLASH${TSC_PRJG_VERSION[$i,5]}$TSC_SLASH$TSC_CFN_CMK"	
	TSC_CPFN_CH="$TSC_CDPA_CONF$TSC_SLASH${TSC_PRJG_VERSION[$i,5]}$TSC_SLASH$TSC_CFN_CH"


	TSC_PACK_VERSION="$TSC_PROJG_${TSC_PRJG_VERSION[$i,4]}"

	for ((j=0; j<3; j++))
	do
		TSC_SPEC_VERSION="$TSC_PROJG_${TSC_PRJG_VERSION[$j,0]}.${TSC_PRJG_VERSION[$j,1]}"
		TSC_SPEC_RELEASE="$TSC_PROJG_${TSC_PRJG_VERSION[$j,2]}-${TSC_PRJG_VERSION[$j,3]}"
		TSC_SPEC_TGZ="${TSC_PRJG_FNAME}${TSC_CDRA_CONF}${TSC_SLASH}${TSC_PRJG_VERSION[$j,5]}$TSC_SLASH$TSC_CDN_TGZ$TSC_SLASH${TSC_PRJG_FNAME}-${TSC_PRJG_VERSION[$j,4]}.src.tgz"
		TSC_SPEC_URL="${TSC_PRJG_FNAME}${TSC_CDRA_CONF}$TSC_SLASH${TSC_PRJG_VERSION[$j,5]}"
		TSC_SPEC_DOC="${TSC_PRJG_FNAME}${TSC_CDRA_CONF}$TSC_SLASH${TSC_PRJG_VERSION[$j,5]}$TSC_SLASH$TSC_CDN_DOC$TSC_SLASH${TSC_PRJG_FNAME}-${TSC_PRJG_VERSION[$j,4]}.pdf"

		TSC_CPFN_SPEC="$TSC_CDPA_CONF$TSC_SLASH${TSC_PRJG_VERSION[$i,5]}$TSC_SLASH$TSC_CDN_SPEC$TSC_SLASH${TSC_PRJG_FNAME}-${TSC_PRJG_VERSION[$j,4]}.${TSC_CTY_SPEC}"
		tsc_write_spec "#" $TSC_PRJG_LANG " " ${TSC_CPFN_SPEC}

		TSC_SNAPYAML_VERSION="$TSC_PROJG_${TSC_PRJG_VERSION[$j,4]}"
		TSC_SNAPYAML_GRADE="$TSC_PROJG_${TSC_PRJG_VERSION[$j,7]}"
		TSC_SNAPYAML_CONFINEMENT="$TSC_PROJG_${TSC_PRJG_VERSION[$j,8]}"
		
		TSC_CPFN_SNAPYAML="$TSC_CDPA_CONF$TSC_SLASH${TSC_PRJG_VERSION[$i,5]}$TSC_SLASH$TSC_CDN_SNAPYAML$TSC_SLASH${TSC_PRJG_FNAME}-${TSC_PRJG_VERSION[$j,4]}.${TSC_CTY_SNAPYAML}"
		tsc_write_snapyaml "#" $TSC_PRJG_LANG " " ${TSC_CPFN_SNAPYAML}

		$TSC_SBP_ECHOE "\nTSC_PROJG_${TSC_PRJG_VERSION[$j,6]}_SPEC="$TSC_CPFN_SPEC >> $TSC_CPFN_CMK
		$TSC_SBP_ECHOE "TSC_PROJG_${TSC_PRJG_VERSION[$j,6]}_SNAPYAML="$TSC_CPFN_SNAPYAML >> $TSC_CPFN_CMK

		$TSC_SBP_ECHOE "\n#define TSC_PROJG_${TSC_PRJG_VERSION[$j,6]}_SPEC "$TSC_CPFN_SPEC >> $TSC_CPFN_CH
		$TSC_SBP_ECHOE "#define TSC_PROJG_${TSC_PRJG_VERSION[$j,6]}_SNAPYAML "$TSC_CPFN_SNAPYAML >> $TSC_CPFN_CH
	done
		
		$TSC_SBP_ECHOE "\nTSC_CPFN_CMK=$TSC_CPFN_CMK" >> $TSC_CPFN_CMK
		$TSC_SBP_ECHOE "TSC_CPFN_CH=$TSC_CPFN_CH\n" >> $TSC_CPFN_CMK
		$TSC_SBP_ECHOE "\n#define TSC_CPFN_CMK $TSC_CPFN_CMK" >> $TSC_CPFN_CH
		$TSC_SBP_ECHOE "#define TSC_CPFN_CH $TSC_CPFN_CH\n" >> $TSC_CPFN_CH

done 
